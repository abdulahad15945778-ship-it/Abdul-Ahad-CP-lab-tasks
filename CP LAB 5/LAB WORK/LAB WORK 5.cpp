#include <iostream>  // Includes the library for input and output operations
using namespace std; // Uses the standard namespace to avoid typing std::

int main()           // The main function where the program starts execution
{
    // A for loop that initializes k to 1, checks if k <= 5, and increments k by 1
    for (int k = 1; k <= 5; k++)
    {
        cout << "Print this line\n"; // Prints the text followed by a new line
    }

    return 0;        // Returns 0 to show the program finished successfully
}
